package marathon2;

public class EscapeSequence {
	public static void main(String[] args) {
		System.out.println("hello\nWorld");
		System.out.println("hello\tworld");
		System.out.println("hello\"world\"");
		System.out.println("hello\'world\'");
		System.out.println("hello\r world");
		System.out.println("");
	}
}
